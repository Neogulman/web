package com.joheul.websocket.model;

public class LogItem {
	private String type;
	private String remote_ip;
	private String event_day;
	private String result;
	private String event;
	private String inout_type;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getRemote_ip() {
		return remote_ip;
	}
	public void setRemote_ip(String remote_ip) {
		this.remote_ip = remote_ip;
	}
	public String getEvent_day() {
		return event_day;
	}
	public void setEvent_day(String event_day) {
		this.event_day = event_day;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public String getInout_type() {
		return inout_type;
	}
	public void setInout_type(String inout_type) {
		this.inout_type = inout_type;
	}
	
	
	

}
